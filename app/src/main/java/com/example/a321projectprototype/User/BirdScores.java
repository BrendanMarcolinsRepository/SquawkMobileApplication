package com.example.a321projectprototype.User;

public class BirdScores {

    private int vulnerable = 0;
    private int endemic = 0;
    private int rareAccidental = 0;
    private int criticallyEndangered = 0;
    private int endangered = 0;
    private int introducedSpecies = 0;
    private int breedingEndemic = 0;
    private int nearThreatened = 0;

    public BirdScores() {
    }

    public int getVulnerable() {
        return vulnerable;
    }

    public void setVulnerable(int vulnerable) {
        this.vulnerable = vulnerable;
    }

    public int getEndemic() {
        return endemic;
    }

    public void setEndemic(int endemic) {
        this.endemic = endemic;
    }

    public int getRareAccidental() {
        return rareAccidental;
    }

    public void setRareAccidental(int rareAccidental) {
        this.rareAccidental = rareAccidental;
    }

    public int getCriticallyEndangered() {
        return criticallyEndangered;
    }

    public void setCriticallyEndangered(int criticallyEndangered) {
        this.criticallyEndangered = criticallyEndangered;
    }

    public int getEndangered() {
        return endangered;
    }

    public void setEndangered(int endangered) {
        this.endangered = endangered;
    }

    public int getIntroducedSpecies() {
        return introducedSpecies;
    }

    public void setIntroducedSpecies(int introducedSpecies) {
        this.introducedSpecies = introducedSpecies;
    }

    public int getBreedingEndemic() {
        return breedingEndemic;
    }

    public void setBreedingEndemic(int breedingEndemic) {
        this.breedingEndemic = breedingEndemic;
    }

    public int getNearThreatened() {
        return nearThreatened;
    }

    public void setNearThreatened(int nearThreatened) {
        this.nearThreatened = nearThreatened;
    }
}
